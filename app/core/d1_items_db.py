# Helper function to get item info
def get_d1_item_info(item_hash: str) -> dict:
    """Get D1 item info by hash"""
    return D1_ITEMS_DB.get(item_hash, None)

def get_items_by_slot(slot: str) -> dict:
    """Get all items for a specific slot"""
    slots_map = {
        "casco": D1_HELMETS,
        "guanti": D1_GAUNTLETS,
        "corazza": D1_CHESTS,
        "gambe": D1_LEGS,
        "cappa": D1_CLASS_ITEMS
    }
    return slots_map.get(slot, {})

def search_items_by_name(name_query: str) -> list:
    """Search items by name (partial match)"""
    results = []
    query_lower = name_query.lower()
    
    for item_hash, item_data in D1_ITEMS_DB.items():
        if query_lower in item_data["name"].lower():
            results.append({"hash": item_hash, **item_data})
    
    return results
